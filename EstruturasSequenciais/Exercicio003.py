##Faça um programa que peça dois numeros e imprima a soma
##Dia1

numero1 = int(input("Digite o primerio numero:"))
numero2 = int(input("Digite o segundo numero:"))

print(f"O primeiro numero é:{numero1}")
print(f"O segundo numero é:{numero2}")

soma = numero1+numero2

print(f"A soma dos dois numeros é:{soma}")